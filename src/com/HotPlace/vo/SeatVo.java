package com.HotPlace.vo;

public class SeatVo {
	private String seatIdx;
	private String seatNum;
	private String pcNum;
	private String seatType;
	private int isChoosen;
	
	public SeatVo() {}

	public SeatVo(String seatIdx, String seatNum, String pcNum, String seatType, int isChoosen) {
		this.seatIdx = seatIdx;
		this.seatNum = seatNum;
		this.pcNum = pcNum;
		this.seatType = seatType;
		this.isChoosen = isChoosen;
	}

	public String getSeatIdx() {
		return seatIdx;
	}

	public void setSeatIdx(String seatIdx) {
		this.seatIdx = seatIdx;
	}

	public String getSeatNum() {
		return seatNum;
	}

	public void setSeatNum(String seatNum) {
		this.seatNum = seatNum;
	}

	public String getPcNum() {
		return pcNum;
	}

	public void setPcNum(String pcNum) {
		this.pcNum = pcNum;
	}

	public String getSeatType() {
		return seatType;
	}

	public void setSeatType(String seatType) {
		this.seatType = seatType;
	}

	public int getIsChoosen() {
		return isChoosen;
	}

	public void setIsChoosen(int isChoosen) {
		this.isChoosen = isChoosen;
	}

	@Override
	public String toString() {
		return "SeatVo [seatIdx=" + seatIdx + ", seatNum=" + seatNum + ", pcNum=" + pcNum + ", seatType=" + seatType
				+ ", isChoosen=" + isChoosen + "]";
	}
	
}
