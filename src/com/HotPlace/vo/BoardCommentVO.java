package com.HotPlace.vo;

import java.sql.Timestamp;

public class BoardCommentVO {
	private int parentNum;
	private int coNum;
	private String coId;
	private String coContent;
	private Timestamp coWritedate;
	
	public BoardCommentVO() {}
	
	public int getParentNum() {
		return parentNum;
	}
	public void setParentNum(int parentNum) {
		this.parentNum = parentNum;
	}
	public int getCoNum() {
		return coNum;
	}
	public void setCoNum(int coNum) {
		this.coNum = coNum;
	}
	public String getCoId() {
		return coId;
	}
	public void setCoId(String coId) {
		this.coId = coId;
	}
	public String getCoContent() {
		return coContent;
	}
	public void setCoContent(String coContent) {
		this.coContent = coContent;
	}
	public Timestamp getCoWritedate() {
		return coWritedate;
	}
	public void setCoWritedate(Timestamp coWritedate) {
		this.coWritedate = coWritedate;
	}

	@Override
	public String toString() {
		return "BoardCommentVO [parentNum=" + parentNum + ", coNum=" + coNum + ", coId=" + coId + ", coContent="
				+ coContent + ", coWritedate=" + coWritedate + "]";
	}
	
	
}
