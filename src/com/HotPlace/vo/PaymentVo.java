package com.HotPlace.vo;

public class PaymentVo {
	int payNum;
	String email;
	int resvNum;
	String payHow;
	String payDate;
	String cardNum;
	String cardCheck;
	String depositor;
	String bank;
	
	public PaymentVo() {}
	
	public PaymentVo(int payNum, String email, int resvNum, String payHow, String payDate, String cardNum,
			String cardCheck, String depositor, String bank) {
		this.payNum = payNum;
		this.email = email;
		this.resvNum = resvNum;
		this.payHow = payHow;
		this.payDate = payDate;
		this.cardNum = cardNum;
		this.cardCheck = cardCheck;
		this.depositor = depositor;
		this.bank = bank;
	}
	
	public int getPayNum() {
		return payNum;
	}
	public void setPayNum(int payNum) {
		this.payNum = payNum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getResvNum() {
		return resvNum;
	}
	public void setResvNum(int resvNum) {
		this.resvNum = resvNum;
	}
	public String getPayHow() {
		return payHow;
	}
	public void setPayHow(String payHow) {
		this.payHow = payHow;
	}
	public String getPayDate() {
		return payDate;
	}
	public void setPayDate(String payDate) {
		this.payDate = payDate;
	}
	public String getCardNum() {
		return cardNum;
	}
	public void setCardNum(String cardNum) {
		this.cardNum = cardNum;
	}
	public String getCardCheck() {
		return cardCheck;
	}
	public void setCardCheck(String cardCheck) {
		this.cardCheck = cardCheck;
	}
	public String getDepositor() {
		return depositor;
	}
	public void setDepositor(String depositor) {
		this.depositor = depositor;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	
	@Override
	public String toString() {
		return "PaymentVo [payNum=" + payNum + ", email=" + email + ", resvNum=" + resvNum + ", payHow=" + payHow
				+ ", payDate=" + payDate + ", cardNum=" + cardNum + ", cardCheck=" + cardCheck + ", depositor="
				+ depositor + ", bank=" + bank + "]";
	}
	
}
