package com.HotPlace.vo;

public class PcVo {
	private int pc_num;
	private String pc_name;
	private String pc_addr;
	private String x;
	private String y;
	private String pc_imf;
	private String pc_seat;
	private String pictureurl;
	private String price;
	
	public PcVo(int pc_num, String pc_name, String pc_addr, String x, String y, String pc_imf, String pc_seat,
			String pictureurl, String price) {
		super();
		this.pc_num = pc_num;
		this.pc_name = pc_name;
		this.pc_addr = pc_addr;
		this.x = x;
		this.y = y;
		this.pc_imf = pc_imf;
		this.pc_seat = pc_seat;
		this.pictureurl = pictureurl;
		this.price = price;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public PcVo() {
		super();
	}


	public String getPictureurl() {
		return pictureurl;
	}



	public void setPictureurl(String pictureurl) {
		this.pictureurl = pictureurl;
	}



	public int getPc_num() {
		return pc_num;
	}
	public void setPc_num(int pc_num) {
		this.pc_num = pc_num;
	}
	public String getPc_name() {
		return pc_name;
	}
	public void setPc_name(String pc_name) {
		this.pc_name = pc_name;
	}
	public String getPc_addr() {
		return pc_addr;
	}
	public void setPc_addr(String pc_addr) {
		this.pc_addr = pc_addr;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getPc_imf() {
		return pc_imf;
	}
	public void setPc_imf(String pc_imf) {
		this.pc_imf = pc_imf;
	}
	public String getPc_seat() {
		return pc_seat;
	}
	public void setPc_seat(String pc_seat) {
		this.pc_seat = pc_seat;
	}
	
}
